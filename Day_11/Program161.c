#include<stdio.h>

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Function Name    : Display
//  Description      : This function accepts a character array and displays each character of the entered string on a new line using array indexing.
//  Input            : Character Array (String)
//  Output           : NONE (Displays characters on console)
//  Author           : Gavade Somanath Vilas
//  Date             : 13/02/2026
//
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

// Function Definition
void Display(char str[])
{
    int i = 0;

    printf("Entered string is : \n");
    
    for(i = 0; str[i] != '\0'; i++)
    {
        printf("%c\n", str[i]);
    }
}

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Application Name : String Character Display Using Indexing
//  Description      : This application accepts a string from the user and displays its characters one by one
//                     using a function implemented with array indexing.
//
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    char Arr[30];

    printf("Enter a String : \n");
    scanf("%[^'\n']s", Arr);

    Display(Arr);           // Function Call.

    return 0;
}
