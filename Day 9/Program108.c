#include<stdio.h>

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Function Name       :   Display
//  Description         :   Displays negative numbers from -1 to -iNo.
//  Input               :   Integer (Number of elements)
//  Output              :   NONE (Displays negative numbers on the screen)
//  Author              :   Gavade Somanath Vilas
//  Date                :   28/01/2026
//
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

// Function Definition
void Display(int iNo)
{
    int iCnt = 0;

    for(iCnt = -1; iCnt >= -iNo; iCnt--)
    {
        printf("%d\t", iCnt);
    }

    printf("\n");
}

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Application Name    :   Negative Number Display Using Function
//  Description         :   This application accepts a number from the user and displays negative numbers starting from -1 up to -iNo.
//
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    int iValue = 0;

    printf("Enter a Number : \n");
    scanf("%d", &iValue);

    Display(iValue);        // Function Call.

    return 0;
}
