#include<stdio.h>

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Function Name    : Display
//  Description      : This function accepts a character pointer and displays each character of the string one by one
//                     until the null terminator ('\0') is reached.
//  Input            : Character Pointer (String)
//  Output           : NONE (Prints characters of the string on console)
//  Author           : Gavade Somanath Vilas
//  Date             : 10/02/2026
//
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

void Display(char *ptr)
{
    while(*ptr != '\0')
    {
        printf("%c\n", *ptr);
        ptr++;
    }
}

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Application Name : String Input and Character Display Using Pointer
//  Description      : This application accepts a string from the user and displays each character one by one using a pointer and a separate Display function.
//
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    char Arr[30];

    printf("Enter your Name : \n");
    scanf("%[^\n]s", Arr);       // Reads input until Enter is pressed

    Display(Arr);
 
    return 0;
}
