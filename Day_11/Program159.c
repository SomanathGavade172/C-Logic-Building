#include<stdio.h>

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Function Name    : Display
//  Description      : This function accepts a character pointer and displays each character of the entered string on a new line using pointer traversal.
//  Input            : Character Pointer (String)
//  Output           : NONE (Displays characters on console)
//  Author           : Gavade Somanath Vilas
//  Date             : 13/02/2026
//
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

// Function Definition
void Display(char *str)
{
    printf("Entered string is : \n");

    while(*str != '\0')
    {
        printf("%c\n", *str);
        str++;
    }
}

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Application Name : String Character Display Using Pointer
//  Description      : This application accepts a string from the user and displays its characters one by one using a function implemented with pointer traversal.
//
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    char Arr[30];

    printf("Enter a String : \n");
    scanf("%[^'\n']s", Arr);

    Display(Arr);           // Function Call.

    return 0;
}
