#include<stdio.h>

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Function Name       :   Display
//  Description         :   Display the message "Jay Ganesh..." given number of times.
//  Input               :   Integer
//  Output              :   Jay Ganesh...
//  Author              :   Gavade Somanath Vilas
//  Date                :   24/12/2025
//
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

// Function Defination
void Display(int iValue)
{
    int iCnt = 0;

    iCnt = 1;

    while(iCnt <= iValue)
    {
        printf("Jay Ganesh..\n");
        iCnt++;
    }
}

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Application Name    :   Display "Jay Ganesh" on console.
//  Description         :   This Application is used to Accept frequency from user and calls Display() function to print thr message.
//  
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    int iFrequency = 0;

    printf("Enter a Frequency : \n");
    scanf("%d", &iFrequency);
    
    Display(iFrequency);        // Function Call

    return 0;
}