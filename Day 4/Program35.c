#include<stdio.h>

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Function Name       :   Display
//  Description         :   Display numbers from 1 to that frequency on the console.
//  Input               :   Integer
//  Output              :   Integer
//  Author              :   Gavade Somanath Vilas
//  Date                :   24/12/2025
//
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

// Function Defination
void Display(int iValue)
{
    int iCnt = 0;

    iCnt = 1;

    while(iCnt <= iValue)
    {
        printf("%d\n", iCnt);
        iCnt++;
    }
}

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Application Name    :   Display Numbers on console.
//  Description         :   This Application is used to Accept frequency from the user and Display Numbers form 1 to that numbers on the console.
//  
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    int iFrequency = 0;

    printf("Enter a Frequency : \n");
    scanf("%d", &iFrequency);
    
    Display(iFrequency);        // Function Call

    return 0;
}