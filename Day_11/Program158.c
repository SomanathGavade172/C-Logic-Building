#include<stdio.h>

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Function Name    : Display
//  Description      : This function accepts a character pointer and displays the entered string on the screen.
//  Input            : Character Pointer (String)
//  Output           : NONE (Displays string on console)
//  Author           : Gavade Somanath Vilas
//  Date             : 10/02/2026
//
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

// Function Definition
void Display(char *str)
{
    printf("Entered string is : %s\n", str);
}

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Application Name : String Display Using Pointer
//  Description      : This application accepts a string from the user and displays it using a function
//                     implemented with pointer traversal.
//
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    char Arr[30];

    printf("Enter a String : \n");
    scanf("%[^'\n']s", Arr);

    Display(Arr);           // Function Call.

    return 0;
}
