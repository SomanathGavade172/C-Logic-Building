#include<stdio.h>

//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Function Name       :   Display 
//  Description         :   Displays numbers from 1 to 10 on the screen.
//  Input               :   None
//  Output              :   None
//  Author              :   Gavade Somanath Vilas
//  Date                :   13/01/2026
//
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

// Function Definition
void Display()
{
    int iCnt = 0;

    for(iCnt = 1; iCnt <= 10; iCnt++)
    {
        printf("%d\t", iCnt);
    }
}

//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Application Name    :   Display Numbers.
//  Description         :   This application calls the Display function to print numbers from 1 to 10 on the console.
//  
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    Display();

    return 0;
}