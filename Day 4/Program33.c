#include<stdio.h>

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Function Name       :   Display
//  Description         :   Display Numbers from 1 to the given frequency.
//  Input               :   Int
//  Output              :   Intger from 1 to Frequency.
//  Author              :   Gavade Somanath Vilas
//  Date                :   25/12/2025
//
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

// Function Defination
void Display(int iNo)
{
    int iCnt = 0;

    for(iCnt = 1; iCnt <= iNo; iCnt++)      // Business Logic
    {
        printf("%d\n", iCnt);
    }
}

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Application Name    :   Display the Numbers.
//  Description         :   This application is used to Accept a Frequency from the user and Display the Numbers from 1 to that frequency.
//
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    int iFrequency = 0;

    printf("Enter the Frequency : \n");
    scanf("%d", &iFrequency);

    Display(iFrequency);        // Function Call

    return 0;
}