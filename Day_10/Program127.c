#include<stdio.h>

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Function Name  : Display
//  Description    : Displays lowercase alphabet pattern starting from 'a' up to the given count.
//  Input          : Integer
//  Output         : None (Displays characters on the console)
//  Author         : Gavade Somanath Vilas
//  Date           : 01/02/2026
//
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

// Function Definition
void Display(int iNo)
{
    int iCnt = 0;
    char ch = 97;

    // Updator.
    if(iNo < 0)
    {
        iNo = -iNo;
    }

    for(iCnt = 1, ch = 'a'; iCnt <= iNo; iCnt++, ch++)
    {
        printf("%c\t", ch);
    }
    printf("\n");
}

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Application Name : Lowercase Alphabet Pattern Display
//  Description      : This application accepts a number from the user and displays lowercase alphabets starting from 'a'.
//
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    int iValue = 0;

    printf("Enter number of elements : \n");
    scanf("%d", &iValue);

    Display(iValue);     // Function Call

    return 0;
}
